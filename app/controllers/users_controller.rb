  # GET /users/new
  def new
    @user = User.new
    @user.line_user_id = params[:line_user_id]
  end

  def hello
    l_id = params[:line_user_id]
    user = User.find_by(line_user_id: l_id)
    if user
      res = {type: 'exist_user', message: "「#{user.name}」さんこんにちは！"}
    else
      res = {type: 'new_user', message: "新規登録をお願いします", url: "https://931fc19a.ngrok.io/users/new?line_user_id=#{l_id}"}
    end
